namespace MermaidPad.Models.Constants;

/// <summary>
/// Provides constant string values for common geometric shape names.
/// </summary>
/// <remarks>This class defines standardized names for various shapes, which can be used to ensure consistency
/// when referring to shapes in code, configuration, or serialization. The constants include both full names and common
/// abbreviations for each shape.</remarks>
internal static class ShapeNames
{
    public const string Cyl = "cyl";
    public const string Cylinder = "cylinder";
    public const string Circ = "circ";
    public const string Circle = "circle";
    public const string Curve = "curve";
    public const string Diamond = "diamond";
    public const string Disk = "disk";
    public const string Hex = "hex";
    public const string Hexagon = "hexagon";
    public const string Polygon = "polygon";
    public const string Rect = "rect";
    public const string Rectangle = "rectangle";
    public const string Rounded = "rounded";
    public const string Square = "square";
    public const string Stadium = "stadium";
}
